    private void populateManageRequestTable() {
        DefaultTableModel model = (DefaultTableModel)tblManageRequest.getModel();
        
        model.setRowCount(0);

        Enterprise enterp = null;
        for (Enterprise ent : network.getEnterpriseDirectory().getEnterpriseList()){
//            System.out.println(ent.getEnterpriseType().getValue()+"<-------here");
            if (ent.getEnterpriseType().getValue().equals("Hospital Enterprise") && ent instanceof HospitalEnterprise){
                
                enterp = ent;
                break;
            }
        }
        
        for(WorkRequest request : enterp.getWorkQueue().getWorkRequestList()){
            Object[] row = new Object[5];
            
            // hardik 5/12
            row[0] = request;
            row[1] = request.getMedicine().getMedicineName();
            row[2] = request.getMedicine().getQuantity();
            row[3] = request.getMedicine().getExpiryDate();
            row[4] = request.getStatus();
            model.addRow(row);
        }

    }
